#include<stdio.h>

int main(void)
{
    int no1;
    int no2;
    int no3;

    printf("Enter Number 1:");
    scanf("%d\n",&no1);

    printf("Enter Number 2:");
    scanf("%d\n",&no2);

    printf("Enter Number 3:");
    scanf("%d\n\n",&no3);

    printf("Enter Number 1 is %d\n",no1);
    printf("Enter Number 2 is %d\n",no2);
    printf("Enter Number 3 is %d\n",no3);

    return 0;
}

