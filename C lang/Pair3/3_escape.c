#include<stdio.h>

int main()
{
    printf("HelloWorld");               //HelloWorld

    printf("\n'HelloWorld\'");            //'HelloWorld'

    printf("\n\"HelloWorld\"");           //"HelloWorld"

    printf("\nHelloWorld\?");             //HelloWorld?

    printf("\nHelloWorld\\");             //HelloWorld\
  
    printf("\nHelloWorld\b");             //backspace

    printf("\nHelloWorld\a");             //beep sound

    printf("\nHelloWorld\f");             //new line 

    printf("\nHelloWorld\n");             //next new line

    printf("\nHelloWorld\r");             //carriage return

    printf("\nHello\tWorld");             //Hello World

    printf("\nHelloWorld\v");             //vertical tab

    printf("\n\0 Hello World");           //null


   return 0;
}
  
   
   