#include<stdio.h>

int main(void)
{ 
     int no1='A';
     int no2=65;
   
     char ch1='A';
     char ch2=65;

     printf("\n%d",no1);       //65
     printf("\n%c",no1);       //A
     printf("\n%d",no2);       //65
     printf("\n%c",no2);       //A
	
     printf("\n\n%d",ch1);     //65
     printf("\n%c",ch1);       //A
     printf("\n%d",ch2);       //65
     printf("\n%c",ch1);       //A
    
    return 0;
}
